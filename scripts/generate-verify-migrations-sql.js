#!/usr/bin/env node
/**
 * Generates scripts/sql/verify-migrations-0001-0008.sql from Prisma migration files.
 * Read-only verification SQL — no database modifications.
 */

const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const MIGRATIONS_DIR = path.join(ROOT, "prisma", "migrations");
const OUT_FILE = path.join(ROOT, "scripts", "sql", "verify-migrations-0001-0008.sql");

const MIGRATION_IDS = [
  "0001_baseline",
  "0002_discoveries",
  "0003_trust_graph",
  "0004_notifications",
  "0005_moderation",
  "0006_platform",
  "0007_security_rbac",
  "0008_media_platform",
];

function parseMigration(migrationId) {
  const sqlPath = path.join(MIGRATIONS_DIR, migrationId, "migration.sql");
  const sql = fs.readFileSync(sqlPath, "utf8");

  const enums = [...sql.matchAll(/CREATE TYPE "([^"]+)" AS ENUM/g)].map((m) => m[1]);

  const tables = [];
  const columns = [];
  for (const match of sql.matchAll(/CREATE TABLE "([^"]+)" \(([\s\S]*?)\n\);/g)) {
    const tableName = match[1];
    tables.push(tableName);
    const body = match[2];
    for (const colMatch of body.matchAll(/^\s*"([^"]+)"\s+/gm)) {
      const colName = colMatch[1];
      if (colName === "CONSTRAINT") continue;
      columns.push({ table: tableName, column: colName });
    }
  }

  const indexes = [...sql.matchAll(/CREATE (?:UNIQUE )?INDEX "([^"]+)"/g)].map((m) => m[1]);

  const fkeys = [
    ...sql.matchAll(/ADD CONSTRAINT "([^"]+)" FOREIGN KEY/g),
  ].map((m) => m[1]);

  return { migrationId, enums, tables, columns, indexes, fkeys };
}

function sqlValueRows(rows, columns) {
  if (rows.length === 0) return "  SELECT NULL WHERE false";
  const lines = rows.map((row) => {
    const vals = columns.map((c) => {
      const v = row[c];
      return typeof v === "string" ? `'${v.replace(/'/g, "''")}'` : String(v);
    });
    return `    (${vals.join(", ")})`;
  });
  return lines.join(",\n");
}

function buildMigrationCte(prefix, migrationId, parsed) {
  const parts = [];

  parts.push(`${prefix}_enums AS (
  SELECT *
  FROM (VALUES
${sqlValueRows(
  parsed.enums.map((name) => ({ migration: migrationId, kind: "enum", name })),
  ["migration", "kind", "name"]
  )}
  ) AS v(migration, kind, name)
)`);

  parts.push(`${prefix}_tables AS (
  SELECT *
  FROM (VALUES
${sqlValueRows(
  parsed.tables.map((name) => ({ migration: migrationId, kind: "table", name })),
  ["migration", "kind", "name"]
  )}
  ) AS v(migration, kind, name)
)`);

  parts.push(`${prefix}_columns AS (
  SELECT *
  FROM (VALUES
${sqlValueRows(
  parsed.columns.map(({ table, column }) => ({
    migration: migrationId,
    table_name: table,
    column_name: column,
  })),
  ["migration", "table_name", "column_name"]
  )}
  ) AS v(migration, table_name, column_name)
)`);

  parts.push(`${prefix}_indexes AS (
  SELECT *
  FROM (VALUES
${sqlValueRows(
  parsed.indexes.map((index_name) => ({ migration: migrationId, index_name })),
  ["migration", "index_name"]
  )}
  ) AS v(migration, index_name)
)`);

  if (parsed.fkeys.length > 0) {
    parts.push(`${prefix}_fkeys AS (
  SELECT *
  FROM (VALUES
${sqlValueRows(
  parsed.fkeys.map((constraint_name) => ({ migration: migrationId, constraint_name })),
  ["migration", "constraint_name"]
  )}
  ) AS v(migration, constraint_name)
)`);
  }

  return parts.join(",\n\n");
}

function main() {
  const parsed = MIGRATION_IDS.map(parseMigration);
  const prefixes = parsed.map((p) => "m" + p.migrationId.replace(/^0+/, "").replace(/_.*/, ""));

  const migrationCtes = parsed
    .map((p, i) => buildMigrationCte(prefixes[i], p.migrationId, p))
    .join(",\n\n");

  const enumUnion = parsed
    .filter((p) => p.enums.length > 0)
    .map((p, i) => `SELECT * FROM ${prefixes[i]}_enums`)
    .join("\n    UNION ALL ");

  const tableUnion = parsed.map((p, i) => `SELECT * FROM ${prefixes[i]}_tables`).join("\n    UNION ALL ");
  const columnUnion = parsed.map((p, i) => `SELECT * FROM ${prefixes[i]}_columns`).join("\n    UNION ALL ");
  const indexUnion = parsed.map((p, i) => `SELECT * FROM ${prefixes[i]}_indexes`).join("\n    UNION ALL ");
  const fkeyUnion = parsed
    .filter((p) => p.fkeys.length > 0)
    .map((p, i) => `SELECT * FROM ${prefixes[i]}_fkeys`)
    .join("\n    UNION ALL ");

  const sql = `-- =============================================================================
-- READ-ONLY production baseline verification for Prisma migrations 0001–0008
-- =============================================================================
-- AUTO-GENERATED by scripts/generate-verify-migrations-sql.js — do not edit by hand.
-- Regenerate after changing migrations 0001–0008:
--   node scripts/generate-verify-migrations-sql.js
--
-- Usage (direct Postgres URL only — not the pooler):
--   psql "$DIRECT_URL" -f scripts/sql/verify-migrations-0001-0008.sql
--
-- This script performs SELECT-only checks. It does NOT modify the database.
-- Run BEFORE any \`prisma migrate resolve --applied\` commands.
-- =============================================================================

WITH
${migrationCtes},

enum_checks AS (
  SELECT
    e.migration,
    'enum'::text AS kind,
    e.name AS object_name,
    EXISTS (
      SELECT 1
      FROM pg_type t
      JOIN pg_namespace n ON n.oid = t.typnamespace
      WHERE n.nspname = 'public'
        AND t.typname = e.name
    ) AS ok
  FROM (
    ${enumUnion || "SELECT NULL::text AS migration, NULL::text AS kind, NULL::text AS name WHERE false"}
  ) e
),
table_checks AS (
  SELECT
    t.migration,
    'table'::text AS kind,
    t.name AS object_name,
    EXISTS (
      SELECT 1
      FROM information_schema.tables ist
      WHERE ist.table_schema = 'public'
        AND ist.table_name = t.name
        AND ist.table_type = 'BASE TABLE'
    ) AS ok
  FROM (
    ${tableUnion}
  ) t
),
column_checks AS (
  SELECT
    c.migration,
    'column'::text AS kind,
    c.table_name || '.' || c.column_name AS object_name,
    EXISTS (
      SELECT 1
      FROM information_schema.columns ic
      WHERE ic.table_schema = 'public'
        AND ic.table_name = c.table_name
        AND ic.column_name = c.column_name
    ) AS ok
  FROM (
    ${columnUnion}
  ) c
),
index_checks AS (
  SELECT
    i.migration,
    'index'::text AS kind,
    i.index_name AS object_name,
    EXISTS (
      SELECT 1
      FROM pg_indexes pi
      WHERE pi.schemaname = 'public'
        AND pi.indexname = i.index_name
    ) AS ok
  FROM (
    ${indexUnion}
  ) i
),
fkey_checks AS (
  SELECT
    f.migration,
    'foreign_key'::text AS kind,
    f.constraint_name AS object_name,
    EXISTS (
      SELECT 1
      FROM information_schema.table_constraints tc
      WHERE tc.constraint_schema = 'public'
        AND tc.constraint_name = f.constraint_name
        AND tc.constraint_type = 'FOREIGN KEY'
    ) AS ok
  FROM (
    ${fkeyUnion}
  ) f
),
all_checks AS (
  SELECT * FROM enum_checks WHERE migration IS NOT NULL
  UNION ALL SELECT * FROM table_checks
  UNION ALL SELECT * FROM column_checks
  UNION ALL SELECT * FROM index_checks
  UNION ALL SELECT * FROM fkey_checks
),
migration_summary AS (
  SELECT
    migration,
    COUNT(*) AS total_checks,
    COUNT(*) FILTER (WHERE ok) AS passed_checks,
    COUNT(*) FILTER (WHERE NOT ok) AS failed_checks,
    CASE WHEN bool_and(ok) THEN 'PASS' ELSE 'FAIL' END AS migration_status
  FROM all_checks
  GROUP BY migration
),
failed_details AS (
  SELECT migration, kind, object_name
  FROM all_checks
  WHERE NOT ok
  ORDER BY migration, kind, object_name
),
gate AS (
  SELECT
    CASE
      WHEN EXISTS (SELECT 1 FROM migration_summary WHERE migration_status = 'FAIL')
        THEN 'STOP — do NOT run prisma migrate resolve until all failures are investigated'
      ELSE 'GO — all migrations 0001–0008 verified; safe to proceed with migrate resolve --applied for 0001–0008, then migrate deploy for 0009_i18n'
    END AS deployment_recommendation,
    NOT EXISTS (SELECT 1 FROM migration_summary WHERE migration_status = 'FAIL') AS all_pass
  FROM migration_summary
  LIMIT 1
)

SELECT 'SUMMARY' AS section, migration, migration_status, passed_checks::text, failed_checks::text, total_checks::text
FROM migration_summary
UNION ALL
SELECT 'FAILURE', migration, kind, object_name, 'FAIL', NULL
FROM failed_details
UNION ALL
SELECT 'GATE', NULL, deployment_recommendation, CASE WHEN all_pass THEN 'PASS' ELSE 'FAIL' END, NULL, NULL
FROM gate
ORDER BY
  CASE section WHEN 'SUMMARY' THEN 1 WHEN 'FAILURE' THEN 2 WHEN 'GATE' THEN 3 END,
  migration NULLS LAST,
  migration_status NULLS LAST;
`;

  fs.writeFileSync(OUT_FILE, sql, "utf8");

  const totals = parsed.reduce(
    (acc, p) => {
      acc.enums += p.enums.length;
      acc.tables += p.tables.length;
      acc.columns += p.columns.length;
      acc.indexes += p.indexes.length;
      acc.fkeys += p.fkeys.length;
      return acc;
    },
    { enums: 0, tables: 0, columns: 0, indexes: 0, fkeys: 0 }
  );

  console.log(`Wrote ${OUT_FILE}`);
  console.log(
    `Checks: ${totals.enums} enums, ${totals.tables} tables, ${totals.columns} columns, ${totals.indexes} indexes, ${totals.fkeys} foreign keys`
  );
}

main();
